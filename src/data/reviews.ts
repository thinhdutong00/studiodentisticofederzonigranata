const getInitials = (name: string) => {
  const words = name
    .replace(/[^\wÀ-ÿ\s.]/g, ' ')
    .split(/\s+/)
    .filter(Boolean);

  if (words.length === 0) return 'FG';
  if (words.length === 1) return words[0].slice(0, 2).toUpperCase();

  return `${words[0][0]}${words[words.length - 1][0]}`.toUpperCase();
};

